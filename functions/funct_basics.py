# Function Basics
# def - means define, declares a functions name

def say_hello():
    print('hello')

say_hello()

# Arguments vs Parameters
# Parameters
#   -variables inside functions
#   -say_hello(Paramenter, parameter)
# Arguments
#   -Actual values passed to the parameters
#   -Calling or invoking a function
#   -say_hello(andrei, tigger)

# Positional Parameters or Aruments
#   -Arguments need to be in order of how the parameters are set
# Default Parameters
#   -When parameters are set with a default value
# Keyword Arguments
#   -Calling parameters with their associated value
#   -say_hello(Paramenter = 'andrei', parameter = 'tigger')

